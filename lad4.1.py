order = input("ป้อนเมนูไข่ที่ต้องการ :")
amount = int(input("ป้อนจำนวนไข่ที่ต้องการ :"))

if order == "ไข่ต้ม":
    print("ลูกค้าต้องจ่ายเงิน = ", amount*5)
elif order == "ไข่ดาว":
    print("ลูกค้าต้องจ่ายเงิน = ", amount*7)
elif order == "ไข่เจียว":
    print("ลูกค้าต้องจ่ายเงิน = ", amount*10)
else:
    print("ร้านเรามีเมนู ไข่ต้ม ไข่ดาว และไข่เจียว")
