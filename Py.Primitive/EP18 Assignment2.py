#เขียนโปรแกรมหาตัวเลข มาก / น้อย

a=int(input("ป้อนตัวเลขที่ 1 :"))
b=int(input("ป้อนตัวเลขที่ 2 :"))

if a>b :
    print(a," มากกว่า ",b) 
else : #ใส่,จะสามารถทำงานได้ เพราะ print จะมองว่าเป็น object หรือ str ทั้งหมด
    print(a," น้อยกว่า ",b)
# ถ้าใส่เป็น + ต้องเปลี่ยน a กับ b เป็น str
    
    
